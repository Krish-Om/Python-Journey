# in and not in operator checks if a element is present in the or right side of the operator
#
# for example:
#
# elem in list
# elem not in list
#
# above two statements will return True and False
# Checks elem is present in list or not
# Checks elem is not in list
#
my_list = [1, 2, 4, 5, 6, 7]


elem = 3
print(elem in my_list)
print(elem not in my_list)
